# variable declaration...
msgText="Hello, world!"

# print text message to output screen...
puts msgText.upcase()   # show text as being all upper case letters
puts msgText.downcase() # show text as being all lower case letters

# NOTE: The pair of empty brackets: () at the end of both upcase()/downcase() are, actually, 'optional';
#       so, writing it as: upcase/downcase...using no pair of brackets would also work.

# Output...
# HELLO, WORLD!
# hello, world!
